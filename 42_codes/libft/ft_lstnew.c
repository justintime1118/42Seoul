#include "libft.h"
#include <stdlib.h>

t_list	*ft_lstnew(void *content)
{
	t_list	*new_elem;

	if (!(new_elem = malloc(sizeof(t_list))))
		return (0);
	new_elem->content = content;
	new_elem->next = NULL;
	return (new_elem);
}
